    public void crearTabla(){
        modelo.addColumn("Tipo");
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Telefono");
        modelo.addColumn("Direccion");
        modelo.addColumn("Correo");
        modelo.addColumn("NÂ° Cuotas");
        
        tblClientes.setModel(modelo);
    }
    
    public void limpiarTabla(){        
        int nf = modelo.getRowCount()-1;
        for (int i = nf; i >= 0; i--) {
            modelo.removeRow(modelo.getRowCount()-1);
        }
    }
    
    private void llenarTabla(){        
        Cliente c;
        Prestamo p;
        String dato[]= new String[7];
        
        for(int i=0;i<JIFRegistroPrestamo.BPres.getPrestamos().size();i++){
            if(JIFRegistroPrestamo.BPres.getPrestamos().get(i).isEstado()==false){
                p = JIFRegistroPrestamo.BPres.getPrestamos().get(i);
                c = p.getCliente();
                
                if(c.getCodigo().length()==8){
                    dato[0]= "N";
                }
                if(c.getCodigo().length()==11){
                    dato[0]= "J";
                }
                
                dato[1]= c.getCodigo();
                dato[2]= c.getNombre();
                dato[3]= c.getTelefono();
                dato[4]= c.getDireccion();
                dato[5]= c.getCorreo();
                dato[6]= String.valueOf(p.getNumeroDeCuotas());
                modelo.addRow(dato);
            }
                
        }
    }

