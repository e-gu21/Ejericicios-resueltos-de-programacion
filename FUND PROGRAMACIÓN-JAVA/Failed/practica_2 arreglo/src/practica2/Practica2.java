package practica2;
import java.io.*;
public class Practica2
{static BufferedReader br=new BufferedReader (new InputStreamReader(System.in));
public static void main (String[]args)throws IOException
{int         A[]=new int [100];
  int         B[]=new int [100];
  int         C[]=new int [100];
  int         D[]=new int [100];
  char         E[] =new char [100];
  char         F[] =new char [100];
  int N,m,ninv=0,res,i;
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
     do{System.out.println( "Ingrese la cantidad de datos: ");
         N=Integer.parseInt(br.readLine());      
         }while (N<=0||N>100);
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////  
 System.out.println("\nIngreso de datos del arreglo A");
  for(i=0;i<N;i++){
      	do{System.out.print("Ingrese A ["+i+"]: ");
    	A[i]=Integer.parseInt(br.readLine());}while(A[i]<=0);  	
  }
 System.out.println("\nIngreso de datos del arreglo B"); 
  for(i=0;i<N;i++){
       do{System.out.print("Ingrese B ["+i+"]: ");
       B[i]=Integer.parseInt(br.readLine());}while(B[i]<=0);  	
  }
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////  
   System.out.println("\nReporte de los datos del arreglo A");
   System.out.println("A");
   for(i=0;i<N;i++){
      System.out.println(+A[i]); }
   System.out.println("\nReporte de los datos del arreglo B");
   System.out.println("B");
   for(i=0;i<N;i++){
   System.out.println(+B[i]); } 
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
 System.out.println("\nReporte de los datos del arreglo C");
 System.out.println("A\tB\tC");
   for(i=0;i<N;i++){C[i]=A[i]+B[i];}
   for(i=0;i<N;i++){
   System.out.println(A[i]+"\t"+B[i]+"\t"+C[i]); }
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////  	
System.out.println("\nReporte de los datos del arreglo D");
System.out.println("A\tB\tD");

  for(i=0;i<N;i++){
  	if(A[i]<B[i]){D[i]=1;
  	}
  	else {D[i]=0;
  	}}
  	 for(i=0;i<N;i++){System.out.println(A[i]+"\t"+B[i]+"\t"+D[i]);} 

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
System.out.println("\nReporte de los datos del arreglo E");
System.out.println("A\tE");

for (i=0;i<N;i++){
	if(A[i]%2==0){ E[i]='p';
	}
	else{E[i]='i';
	}
}
for(i=0;i<N;i++){System.out.println(A[i]+"\t"+E[i]);}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////    	
System.out.println("\nReporte de los datos del arreglo F");
System.out.println("B\tF");

for (i=0;i<N;i++){
	m=B[i];
	
	while(B[i]>0){
		res=B[i]%10;
		ninv=ninv*10+res;
		B[i]=B[i]/10;
	}
	if(ninv==m){	F[i]='C';
	System.out.println(m+"\t"+F[i]);}
	else {F[i]='N';
	System.out.println(m+"\t"+F[i]);	}
	
	}
}
}
